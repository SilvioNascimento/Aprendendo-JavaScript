let frutas = ['Uva', 'Maçã', 'Banana', 'Pêra', 'Abacaxi'];

// Deixa os itens no array de forma inversa, se usado antes de '.sort()'
frutas.reverse();
console.log(frutas)

// Deixa os itens no array de ordem alfabética ascendente
frutas.sort();
console.log(frutas);

// Deixa os itens no array de ordem alfabética descendente, se usado depois de '.sort()'
frutas.reverse();
console.log(frutas)