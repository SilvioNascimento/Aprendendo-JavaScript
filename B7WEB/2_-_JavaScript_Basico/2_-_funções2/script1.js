// Arrow Function
const somar = (n1, n2) => {
    return n1 + n2;
}

console.log(somar(5, 8));


// Arrow Function Simplificado
const subtrair = (x, y) => x - y;
console.log(subtrair(5, 8));


// Arrow Function com 1 parâmetro
const dobrar = n => n * 2;
console.log(dobrar(5));


// Arrow Function sem parâmetros
const ola = () => 'Olá, pessoal!';
console.log(ola());